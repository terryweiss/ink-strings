/**
 * This pattern tests for a hexadecimal string
 * @type {RegExp}
 */
exports.HEXPATTERN = /[^a-fA-F0-9]/;
/**
 * This pattern looks for only alphanumerics
 * @type {RegExp}
 */
exports.ANUMPATTERN = /[^a-zA-Z0-9]/;
/**
 * This pattern looks only for alphabetic characters
 * @type {RegExp}
 */
